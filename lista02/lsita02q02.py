# Informe o tamanho do lado do quadrado

tamanho_do_lado = int(input('Qual é o tamanho do lado do quadrado? '))

# Calculando a área e o perímetro do quadrado

area = tamanho_do_lado ** 2
perimetro = 4 * tamanho_do_lado

# Imprimindo os resultados da área e perímetro do quadrado

print('A área do quadrado é: {} e seu perímetro é: {}'.format(area,perimetro))