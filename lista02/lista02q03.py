
# Informe os números da base e do expoente

base = int(input('Qual é a base da potência? '))
expoente = int(input('Qual é o expoente da potência? '))

# Calculando a potência

potencia = base ** expoente

# Imprimindo o valor da potência

print('A potência dos números informados é: {}'.format(potencia))